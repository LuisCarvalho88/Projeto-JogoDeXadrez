using tabuleiro;

namespace xadrez
{
    class Rei : Peca // rei é um herança de peca
    {
        public Rei(Tabuleiro tab, Cor cor) : base(tab, cor)
        {
        }
        public override string ToString()
        {
            return "R";
        }

        private bool podeMover(Posicao pos)
        {
            Peca p = tab.peca(pos); //pega e retorna a peca que está na posição, se for null a casa ta livra ou se a cor da peça for diferente da cor do rei ou seja, uma peça advesária
            return p == null || p.cor != cor;
        }

        public override bool[,] MovimentosPossiveis() //movimentos possiveis do rei Henrança e sobreposição
        {
            bool[,] mat = new bool[tab.linhas, tab.colunas]; //estrutura de dados matriz
            Posicao pos = new Posicao(0, 0);

            //verificando acima da posicao atual do REI
            pos.definirValores(posicao.linha - 1, posicao.coluna);
            if (tab.posicaoValida(pos) && podeMover(pos))
            {
                mat[pos.linha, pos.coluna] = true;
            }
            //verificando acima direita da posicao atual do REI
            pos.definirValores(posicao.linha - 1, posicao.coluna + 1);
            if (tab.posicaoValida(pos) && podeMover(pos))
            {
                mat[pos.linha, pos.coluna] = true;
            }
            //verificando direita da posicao atual do REI
            pos.definirValores(posicao.linha, posicao.coluna + 1);
            if (tab.posicaoValida(pos) && podeMover(pos))
            {
                mat[pos.linha, pos.coluna] = true;
            }
            //verificando abaixo direita da posicao atual do REI
            pos.definirValores(posicao.linha + 1, posicao.coluna + 1);
            if (tab.posicaoValida(pos) && podeMover(pos))
            {
                mat[pos.linha, pos.coluna] = true;
            }
            //verificando abaixo da posicao atual do REI
            pos.definirValores(posicao.linha + 1, posicao.coluna);
            if (tab.posicaoValida(pos) && podeMover(pos))
            {
                mat[pos.linha, pos.coluna] = true;
            }
            //verificando abaixo esquerda da posicao atual do REI
            pos.definirValores(posicao.linha + 1, posicao.coluna - 1);
            if (tab.posicaoValida(pos) && podeMover(pos))
            {
                mat[pos.linha, pos.coluna] = true;
            }
            //verificando esquerda da posicao atual do REI
            pos.definirValores(posicao.linha, posicao.coluna - 1);
            if (tab.posicaoValida(pos) && podeMover(pos))
            {
                mat[pos.linha, pos.coluna] = true;
            }
            //verificando acima esquerda da posicao atual do REI
            pos.definirValores(posicao.linha - 1, posicao.coluna - 1);
            if (tab.posicaoValida(pos) && podeMover(pos))
            {
                mat[pos.linha, pos.coluna] = true;
            }
            return mat;
        }
    }
}
